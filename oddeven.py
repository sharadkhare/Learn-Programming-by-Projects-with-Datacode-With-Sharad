# Python Program to Check if a Number is Odd or Even. # In this example, you will learn to check whether a number entered by the user is even or odd.

sharad = int(input("Enter a Number: "))
if (sharad % 2 )==0:
    print("{0} is a Even Number".format(sharad))
else:
    print("{0} is a Odd Number".format(sharad))